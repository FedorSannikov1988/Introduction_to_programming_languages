void PrintIntMatrix(int[,] matrix)
{
    for (int line = 0; line < matrix.GetLength(0); line++)
    {
        for (int column = 0; column < matrix.GetLength(1); column++)
        {
            System.Console.Write($" {matrix[line, column]} ");
        }

        System.Console.WriteLine();
    }
}

void PrintImage(int[,] matrix)
{
    for (int line = 0; line < matrix.GetLength(0); line++)
    {
        for (int column = 0; column < matrix.GetLength(1); column++)
        {
            if (matrix[line, column] == 0)
            {
                System.Console.Write(" ");
            }
            else
            {
                System.Console.Write("*");
            }
        }
        System.Console.WriteLine();
    }
}

void FillImage(int[,] matrix, int line, int column)
{
    if (matrix[line, column] == 0) 
    {
        matrix[line, column] = 1;

        FillImage(matrix, line - 1, column);
        FillImage(matrix, line + 1, column);

        FillImage(matrix, line, column - 1);
        FillImage(matrix, line, column + 1);
    }
}

